function verificarIdade(idade) {
    if (idade >= 18) {
        console.log("Você é maior de idade.");
    } else {
        console.log("Você é menor de idade.");
    }
}

// Exemplo de uso:
verificarIdade(20); // Saída: "Você é maior de idade."
verificarIdade(16); // Saída: "Você é menor de idade."